class DataType {
    public static void main (String [] args){
        // integer
        int numberHundred = 100;
        System.out.println(numberHundred);

        // negative integer
        int negativeValue = -230;
        System.out.println(negativeValue);

        // long value 2
        long longValue1 = 123543L;
        System.out.println(longValue1);

        // long value 2
        long longValue2 = -9876345;
        System.out.println(longValue2);

        // double value
        double doubleValue = 12.25;
        System.out.println(doubleValue);

        // double value
        double doubleValue1 = 12345.234;
        System.out.println(doubleValue1);

        // boolean value
        boolean boolValue = true;
        System.out.println(boolValue);

        // boolean value
        boolean boolValue1 = false;
        System.out.println(boolValue1);

        // char value
        char charValue = 'j';
        System.out.println(charValue);

        // char value for 88
        char charValue1 = (char) 88;
        System.out.println(charValue1);

        System.out.println("END");
        System.out.println("Change in new branch");
    

    }
}